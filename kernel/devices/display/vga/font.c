VGAfont* vgaFont_getFont(VGAfontType font) {
    return font >= VGA_FONT_TYPE_NUM ? NULL : _vgaFont_fonts[font];
}

void vgaFont_loadFont(VGAfont* font, VGAhardwareRegisters* registers) {
    vgaHardwareRegisters_getFontAccess(registers);

    void* fontDataSrc = font->fontData;
    void* fontDataDes = PAGING_CONVERT_IDENTICAL_ADDRESS_P2V((void*)0xA0000);
    for (int i = 0; i < 256; ++i) {
        memory_memcpy(fontDataDes, fontDataSrc, font->characterHeight);
        fontDataSrc = fontDataSrc + font->characterHeight;
        fontDataDes = fontDataDes + 32;
    }

    vgaHardwareRegisters_releaseFontAccess(registers);
}